const defaultCache = {};
let currentLang = 'ja';

document.addEventListener('DOMContentLoaded', () => {
    // Cache default Japanese text
    for (const selector in enMappings) {
        const el = document.querySelector(selector);
        if (el) {
            defaultCache[selector] = el.innerHTML;
        }
    }

    // Attach lang switcher
    const langSelect = document.getElementById('lang-select');
    if (langSelect) {
        langSelect.addEventListener('change', (e) => {
            currentLang = e.target.value;
            applyTranslations(currentLang);
        });
    }
});

function applyTranslations(lang) {
    document.documentElement.lang = lang;
    for (const selector in enMappings) {
        const el = document.querySelector(selector);
        if (el) {
            if (lang === 'en') {
                el.innerHTML = enMappings[selector];
            } else {
                if (defaultCache[selector]) {
                    el.innerHTML = defaultCache[selector];
                }
            }
        }
    }

    if (lang === 'en') {
        document.body.classList.add('lang-en');
    } else {
        document.body.classList.remove('lang-en');
    }
}
