const defaultCache = {};
let currentLang = 'ja';

document.addEventListener('DOMContentLoaded', () => {
    // Cache default Japanese text
    const allSelectors = new Set();
    for (const lang in translations) {
        for (const sel in translations[lang]) {
            allSelectors.add(sel);
        }
    }
    for (const selector of allSelectors) {
        const el = document.querySelector(selector);
        if (el) {
            defaultCache[selector] = el.innerHTML;
        }
    }

    // Attach lang switcher
    const langSelect = document.getElementById('lang-select');
    if (langSelect) {
        langSelect.addEventListener('change', (e) => {
            currentLang = e.target.value;
            applyTranslations(currentLang);
        });
    }

    // Hero slideshow
    initHeroSlideshow();
});

function applyTranslations(lang) {
    document.documentElement.lang = lang;

    // Remove all lang classes
    document.body.classList.remove('lang-en', 'lang-zh', 'lang-hi');

    if (lang === 'ja') {
        // Restore defaults
        for (const selector in defaultCache) {
            const el = document.querySelector(selector);
            if (el) el.innerHTML = defaultCache[selector];
        }
    } else {
        const mapping = translations[lang];
        if (!mapping) return;

        // First restore defaults, then apply translation
        for (const selector in defaultCache) {
            const el = document.querySelector(selector);
            if (el) el.innerHTML = defaultCache[selector];
        }
        for (const selector in mapping) {
            const el = document.querySelector(selector);
            if (el) el.innerHTML = mapping[selector];
        }
        document.body.classList.add('lang-' + lang);
    }
}

// Hero slideshow
function initHeroSlideshow() {
    const slides = document.querySelectorAll('.hero-slide');
    if (slides.length < 2) return;
    let current = 0;
    setInterval(() => {
        slides[current].classList.remove('active');
        current = (current + 1) % slides.length;
        slides[current].classList.add('active');
    }, 4000);
}
